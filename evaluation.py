import pandas as pd
from sklearn.manifold import trustworthiness

# 1. Chargement
df_original = pd.read_csv('city_lifestyle_dataset.csv').select_dtypes(include=['number']).dropna()
X_tsne = pd.read_csv('tsne_emb_2d.csv')
X_pca = pd.read_csv('X_pca_2D.csv')

# 2. Correction : on s'assure que tout le monde a le même nombre de lignes
# On prend le nombre de lignes le plus petit pour éviter l'IndexError
min_rows = min(len(df_original), len(X_tsne), len(X_pca))

X_original = df_original.iloc[:min_rows]
X_tsne_final = X_tsne.iloc[:min_rows]
X_pca_final = X_pca.iloc[:min_rows]

# 3. Calcul (n_neighbors=10 comme demandé)
score_tsne = trustworthiness(X_original, X_tsne_final, n_neighbors=10)
score_pca = trustworthiness(X_original, X_pca_final, n_neighbors=10)

# 4. Affichage
print(f"Trustworthiness t-SNE : {score_tsne:.4f}")
print(f"Trustworthiness PCA   : {score_pca:.4f}")

"""Analyse des scores :

t-SNE (0.9974) : Ce score est proche de 1. Cela signifie que le t-SNE a presque parfaitement préservé les relations de voisinage local.

PCA (0.6876) : Ce score est beaucoup plus faible. Cela indique que la PCA, étant une méthode linéaire, a perdu une partie importante de la structure locale en projetant les données en 2D.


Cela confirme que pour ce dataset, une méthode non-linéaire est bien plus efficace pour préserver la structure réelle des données lors d'une réduction en 2D.
"""